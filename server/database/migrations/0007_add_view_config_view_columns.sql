-- Create the normalized per-view table.
-- Each row is a single view; a dataset that exposes multiple views (e.g. map + gallery)
-- becomes multiple rows that share the same primary_dataset.
CREATE TABLE views (
  view_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  -- view_name is always set by every writer (backfill below coalesces to table_name;
  -- app writes coalesce to primary_dataset), so it is NOT NULL — readers never need a
  -- fallback for a missing display name.
  view_name text NOT NULL,
  view_type text NOT NULL,
  primary_dataset text NOT NULL,
  secondary_dataset text,
  view_config text NOT NULL,
  -- A dataset exposes each view type at most once, so (view_type, primary_dataset)
  -- is the logical key. Enforce it in the DB so a primary_dataset can never get two rows of
  -- the same view type.
  CONSTRAINT views_view_type_primary_dataset_unique UNIQUE (view_type, primary_dataset)
);
